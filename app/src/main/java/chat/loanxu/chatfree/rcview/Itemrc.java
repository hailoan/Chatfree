public class Itemrc {
    private int img;
    private String noidung;

    public Itemrc() {

    }

    public Itemrc(int img, String noidung) {
        this.img = img;
        this.noidung = noidung;
    }

    public int getImg() {
        return img;
    }

    public String getNoidung() {
        return noidung;
    }
}
